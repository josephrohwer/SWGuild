package com.mycompany.shapesandperimeters;

/**
 *
 * @author josephrohwer
 */
public class Rectangle extends Shape {
    private double a;
    private double b;

    public Rectangle(double a, double b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public double area() {
       return (a * b); 
    }
    
    @Override
    public double perimeter() {
        return (a * 2) + (b * 2);
    }
    
    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }
}
